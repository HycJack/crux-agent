func TestParseWorkflowBlocks_InvalidBlocks(t *testing.T) {
	// Missing NAME → invalid
	resp := `WORKFLOW_START
TRIGGER: 用户要求测试
STEP: 步骤 1
STEP: 步骤 2
STEP: 步骤 3
WORKFLOW_END`
	if skills := parseWorkflowBlocks(resp); len(skills) != 0 {
		t.Errorf("invalid block (no NAME) should be skipped, got %d", len(skills))
	}

	// No STEPs → invalid
	resp2 := `WORKFLOW_START
NAME: no-steps
TRIGGER: 用户要求
WORKFLOW_END`
	if skills := parseWorkflowBlocks(resp2); len(skills) != 0 {
		t.Errorf("invalid block (no STEPS) should be skipped, got %d", len(skills))
	}
}

func TestParseSkillMdBlocks(t *testing.T) {
	resp := `分析：这次对话体现了一个 TDD 工作流。

SKILL_START
---
name: test-driven-go
description: "强制 TDD 流程. Use when the user asks for Go functions."
---

# test-driven-go

> **Use when** the user asks for Go functions

## 步骤
1. 定义函数签名
2. 写失败测试
3. 实现函数

> Auto-generated by autolearn from conversation
SKILL_END

下一段说明...`

	blocks := parseSkillMdBlocks(resp)
	if len(blocks) != 1 {
		t.Fatalf("expected 1 block, got %d", len(blocks))
	}
	if !strings.Contains(blocks[0], "name: test-driven-go") {
		t.Errorf("block missing name frontmatter: %q", blocks[0])
	}
	if !strings.Contains(blocks[0], "Auto-generated by autolearn") {
		t.Errorf("block missing autogen footer: %q", blocks[0])
	}
}

