func TestSanitizeName(t *testing.T) {
	tests := []struct {
		in, want string
	}{
		{"Test Driven Go", "test-driven-go"},
		{"my_skill!", "my_skill"},
		{"hello world---foo", "hello-world-foo"},
		{"---leading-and-trailing---", "leading-and-trailing"},
		{"中文 skill", "skill"},
		{"ABC", "abc"},
		// Length cap
		{"this-is-a-very-very-long-skill-name-that-exceeds-sixty-characters-limit", ""},
	}
	for _, tt := range tests {
		got := sanitizeName(tt.in)
		// For length-cap case we expect either empty or shortened.
		if tt.want != "" && got != tt.want {
			t.Errorf("sanitizeName(%q) = %q, want %q", tt.in, got, tt.want)
		}
		if strings.ContainsAny(got, " \t\n!@#$%^&*()") {
			t.Errorf("sanitizeName(%q) = %q contains illegal chars", tt.in, got)
		}
	}
}

func TestParseWorkflowBlocks_SingleBlock(t *testing.T) {
	resp := `WORKFLOW_START
NAME: test-driven-go
TRIGGER: 用户要求写 Go 函数时
DESCRIPTION: 强制 TDD 流程
STEP: 定义函数签名
STEP: 写失败测试
STEP: 实现函数直到测试通过
OUTPUT: 通过测试的 Go 代码
SOURCE: 用户在 3 次对话中都先写测试再实现
WORKFLOW_END`

	skills := parseWorkflowBlocks(resp)
	if len(skills) != 1 {
		t.Fatalf("expected 1 skill, got %d", len(skills))
	}
	s := skills[0]
	if s.Name != "test-driven-go" {
		t.Errorf("Name = %q", s.Name)
	}
	if s.Trigger != "用户要求写 Go 函数时" {
		t.Errorf("Trigger = %q", s.Trigger)
	}
	if s.Description != "强制 TDD 流程" {
		t.Errorf("Description = %q", s.Description)
	}
	if len(s.Steps) != 3 {
		t.Fatalf("Steps count = %d, want 3", len(s.Steps))
	}
	if s.Steps[0] != "定义函数签名" {
		t.Errorf("Steps[0] = %q", s.Steps[0])
	}
	if s.Output != "通过测试的 Go 代码" {
		t.Errorf("Output = %q", s.Output)
	}
	if s.Source != "用户在 3 次对话中都先写测试再实现" {
		t.Errorf("Source = %q", s.Source)
	}
}

