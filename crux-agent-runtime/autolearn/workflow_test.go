func TestMaybeExtractWorkflow_FallbackPath(t *testing.T) {
	tmp := t.TempDir()
	dir := filepath.Join(tmp, "skills")

	settings := DefaultSettings()
	settings.AutoLearn = true
	al := New(nil, settings)
	al.SetWorkflowDir(dir)

	// No SkillWriterDoc → fallback to Extract (structural).
	ext := &WorkflowExtractor{
		SummarizeFunc: func(ctx context.Context, prompt string) (string, error) {
			return `WORKFLOW_START
NAME: tdd-go
TRIGGER: user asks for Go functions
STEP: write test
STEP: run test
STEP: implement
OUTPUT: passing test
WORKFLOW_END`, nil
		},
	}

	n := al.MaybeExtractWorkflow(context.Background(), nil, ext)
	if n != 1 {
		t.Fatalf("expected 1 skill written, got %d", n)
	}
	path := filepath.Join(dir, "tdd-go", "SKILL.md")
	if _, err := os.Stat(path); err != nil {
		t.Errorf("expected SKILL.md at %s: %v", path, err)
	}
}

