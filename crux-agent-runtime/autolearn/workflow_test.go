func TestSkillRenderSKILLMd(t *testing.T) {
	s := Skill{
		Name:        "test-driven-go",
		Trigger:     "asks for Go functions",
		Description: "强制 TDD 流程",
		Steps:       []string{"定义函数签名", "写失败测试", "实现函数"},
		Output:      "通过测试的 Go 代码",
		Source:      "用户在 3 次对话中重复此流程",
	}
	out := s.RenderSKILLMd()

	checks := []string{
		"name: test-driven-go",
		"description:",
		"Use when",
		"# test-driven-go",
		"## 概述",
		"强制 TDD 流程",
		"## 步骤",
		"1. 定义函数签名",
		"2. 写失败测试",
		"3. 实现函数",
		"## 输出",
		"通过测试的 Go 代码",
		"用户在 3 次对话中重复此流程",
	}
	for _, c := range checks {
		if !strings.Contains(out, c) {
			t.Errorf("RenderSKILLMd missing %q\n--- output ---\n%s", c, out)
		}
	}
}

func TestSkillWriteSKILLMd(t *testing.T) {
	tmp := t.TempDir()
	s := Skill{
		Name:        "test-driven-go",
		Trigger:     "asks for Go functions",
		Description: "TDD 流程",
		Steps:       []string{"a", "b", "c"},
	}
	path, err := s.WriteSKILLMd(tmp)
	if err != nil {
		t.Fatalf("WriteSKILLMd failed: %v", err)
	}
	wantPath := filepath.Join(tmp, "test-driven-go", "SKILL.md")
	if path != wantPath {
		t.Errorf("path = %q, want %q", path, wantPath)
	}
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("ReadFile failed: %v", err)
	}
	if !strings.Contains(string(data), "name: test-driven-go") {
		t.Errorf("written file missing frontmatter: %q", string(data))
	}
}

